#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_print_alphabet(void)
{
	int	x;

	x = 0;
	while (x < 26)
	{
		ft_putchar(x + 97);
		x++;
	}
}
